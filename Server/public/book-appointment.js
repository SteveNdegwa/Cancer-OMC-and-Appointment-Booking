function morning() {
    if (document.getElementById('morning-check').checked) {
        document.getElementById('after-morning-check').checked = false;
        document.getElementById('afternoon-check').checked = false;
        afterMorning();
        afternoon();

    }
}

function afterMorning() {
    if (document.getElementById('after-morning-check').checked) {
        document.getElementById('morning-check').checked = false;
        document.getElementById('afternoon-check').checked = false;
        morning();
        afternoon();

    }
}

function afternoon() {
    if (document.getElementById('afternoon-check').checked) {
        document.getElementById('morning-check').checked = false;
        document.getElementById('after-morning-check').checked = false;
        morning();
        afterMorning();
    }
}


